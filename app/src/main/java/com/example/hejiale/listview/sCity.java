package com.example.hejiale.listview;

public class sCity {
    private String name;
    private String code;

    public sCity(String name,String code){//构造函数

        this.name=name;
        this.code=code;

    }

    public String getName() {
        return name;
    }

    public String getCode() {
        return code;
    }
}
